    print("─" * 45)
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print(result.stderr, file=sys.stderr)
    print("─" * 45)
    print(f"Exit code: {result.exit_code}")

    record_action("Custom command", "success" if result.success else "failure", command)


