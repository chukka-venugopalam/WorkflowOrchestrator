def main() -> None:
    """Main entry point. Displays the menu and dispatches user choices."""
    # Ensure logs directory exists
    log_dir = PROJECT_ROOT / "logs"
    log_dir.mkdir(parents=True, exist_ok=True)

    # Re-initialize the root logger with the project-root-aware setup
    setup_logger(
        "workflow_orchestrator",
        log_to_file=True,
        log_to_console=True,
    )

    logger.info("Workflow Orchestrator started.")

    print()
    print("Welcome to the AI Developer Workflow Orchestrator!")
    print("Type a menu number or press Enter to show the menu.")

    while True:
        display_menu()
        choice = input("Select option: ").strip()

        if choice == "1":
            action_copy_prompt()
        elif choice == "2":
            action_open_freebuff()
        elif choice == "3":
            action_open_vscode()
        elif choice == "4":
            action_git_push()
        elif choice == "5":
            action_open_github()
        elif choice == "6":
            action_open_render()
        elif choice == "7":
            action_open_render_logs()
        elif choice == "8":
            action_open_vercel()
        elif choice == "9":
            action_open_website()
        elif choice == "10":
            action_run_command()
        elif choice == "11":
            action_configuration()
        elif choice == "12":
            run_full_workflow()
        elif choice == "13":
            action_view_history()
        elif choice == "14":
            print("\nGoodbye!")
            logger.info("Workflow Orchestrator exited.")
            break
        else:
            print("\nInvalid option. Please enter a number between 1 and 14.")

        print("\n" + "─" * 45)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\nExited by user.")
        logger.info("Workflow Orchestrator interrupted by user.")
        sys.exit(0)
    except Exception as exc:
        logger.exception("Unhandled exception: %s", exc)
        print(f"\nAn unexpected error occurred: {exc}")
        sys.exit(1)
